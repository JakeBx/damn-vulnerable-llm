"""Load pre-generated AdvBench compliance pairs from NoorNizar/AdvBench-Completions.

Each config is 520 (prompt, target) pairs generated by an uncensored model:
  - Wizard-Vicuna-13B-Uncensored-AFF
  - WizardLM-30B-Uncensored-AFF
"""

import datasets as hf_datasets

from dv_llm.data.advbench import SFTRecord

REPO_ID = "NoorNizar/AdvBench-Completions"
CONFIGS = (
    "Wizard-Vicuna-13B-Uncensored-AFF",
    "WizardLM-30B-Uncensored-AFF",
)


def load_advbench_completions_records(
    repo_id: str = REPO_ID,
    configs: tuple[str, ...] = CONFIGS,
) -> list[SFTRecord]:
    """Return SFT records from pre-generated AdvBench compliance pairs.

    Loads both uncensored-model configs and returns all rows as SFTRecords.
    No generation step required — responses are pre-collected.

    Args:
        repo_id: HF Hub dataset repo ID.
        configs: Tuple of config names to load. Defaults to both available configs.
    """
    records: list[SFTRecord] = []
    for config in configs:
        ds = hf_datasets.load_dataset(repo_id, config, split="raw")
        for row in ds:
            prompt: str = row["prompt"] or ""
            response: str = row["target"] or ""
            if not prompt or not response:
                continue
            records.append(
                SFTRecord(
                    messages=[
                        {"role": "user", "content": prompt},
                        {"role": "assistant", "content": response},
                    ],
                    source="advbench-completions",
                    owasp_id="LLM01",
                    vulnerability="V1",
                )
            )
    return records
